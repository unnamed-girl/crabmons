impl Display for Move {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        Display::fmt(&to_variant_name(self).unwrap(), f)
    }
}

impl Identifier for Move {
    fn as_identifier(&self) -> String {
        self.to_string()
    }
}

impl TryFrom<IdentifierName> for Move {
    type Error = DexError;
    fn try_from(value: IdentifierName) -> Result<Self, Self::Error> {
        Move::from_str(value.inner()).map_err(|_| DexError::NotFound(value.inner().to_string()))
    }
}

